// final, constant
void main() {

  int randomNumber = 12;
  randomNumber = 45;
  print(randomNumber);

  /// constant
  // const / Compile-time constant
  const double pi = 3.1416;
  print(pi);

  // final / Runtime-time constant
  final String userName;
  userName = 'RafatMeraz';
  print(userName);
}